      <li>при назначении на должности гражданской службы категории &quot;руководители&quot;, назначение на которые и освобождение от которых осуществляются Президентом Российской Федерации или Правительством Российской Федерации;</li>
     
      <li>при заключении срочного служебного контракта;</li>
     
      <li>при назначении гражданского служащего на иную должность гражданской службы в случаях, предусмотренных частью 2 статьи 28 и частями 1, 2 и 3 статьи 31 настоящего Федерального закона;</li>
     
      <li>при назначении на должность гражданской службы гражданского служащего (гражданина), состоящего в кадровом резерве, сформированном на конкурсной основе.</li>
     </ol>
   
