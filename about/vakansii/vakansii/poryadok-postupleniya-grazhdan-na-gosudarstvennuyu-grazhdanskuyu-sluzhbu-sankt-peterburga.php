  <h3>Порядок поступления на гражданскую службу предусмотрен статьями 21 и 22 Федерального закона № 79-ФЗ от 27.07.2004 «О государственной гражданской службе Российской Федерации»</h3>

  <p>Статья 21. Право поступления на гражданскую службу</p>

  <ol>
    <li>На гражданскую службу вправе поступать граждане Российской Федерации, достигшие возраста 18 лет, владеющие государственным языком Российской Федерации и соответствующие квалификационным требованиям, установленным настоящим Федеральным законом.</li>
  </ol>

  <p>Статья 22. Поступление на гражданскую службу и замещение должности гражданской службы по конкурсу</p>

  <ol> 
    <li>Поступление гражданина на гражданскую службу для замещения должности гражданской службы или замещение гражданским служащим другой должности гражданской службы осуществляется по результатам конкурса, если иное не установлено настоящей статьей. Конкурс заключается в оценке профессионального уровня претендентов на замещение должности гражданской службы, их соответствия установленным квалификационным требованиям к должности гражданской службы.</li>
   
    <li>Конкурс не проводится:</li>
   
